    private void doConnect() {
        try {
            this.conn = DriverManager.getConnection(url, user, password);
            System.out.println("Connection to MySQL established successfully!");
            // Perform database operations here
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,"Error en conexión con la BD: "+e.getMessage(),"Encuesta",JOptionPane.ERROR_MESSAGE);
            System.err.println("Error connecting to MySQL: " + e.getMessage());
        }
    }

    private void guardarDB(String sSO, String sPrg, String sGrf, String sAdm, int iHrs) {
        Statement stmt;
        String sentIns = String.format("INSERT INTO respuestas (sSisOper,cProgra,cDiseno,cAdmon,iHoras) VALUES ('%s','%s','%s','%s',%d)",sSO,sPrg,sGrf,sAdm,iHrs);
        try {
            stmt = this.conn.createStatement();
            stmt.execute(sentIns);
        } catch (SQLException ex) {
            System.getLogger(Encuesta.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            JOptionPane.showMessageDialog(this,"No hay conexión con la BD","Encuesta",JOptionPane.ERROR_MESSAGE);
        }        
    }
