    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String sRes = "";
        String sSO = "";
        String sPrg = "N";
        String sGrf = "N";
        String sAdm = "N";
        int iHrs = 0;
        
        sSO = "Windows";
        if (this.rbLnx.isSelected()) sSO = "Linux";
        if (this.rbMac.isSelected()) sSO = "Mac";

        if (this.ckPrg.isSelected()) sPrg = "S";
        if (this.ckGrf.isSelected()) sGrf = "S";
        if (this.ckAdm.isSelected()) sAdm = "S";
        
        iHrs = this.slHoras.getValue();
        
        sRes = String.format("%s,%s,%s,%s,%d\n",sSO,sPrg,sGrf,sAdm,iHrs);
        
        try { 
            // Create a FileWriter to write to a file named "example.txt"
            FileWriter w = new FileWriter("respuestas.txt",true);
            // Write a simple message to the file
            w.write(sRes);
            // Close the writer
            w.close();

            JOptionPane.showMessageDialog(this, "Mensaje guardado","Encuesta",JOptionPane.INFORMATION_MESSAGE);
        }
        catch (IOException e) {
            JOptionPane.showMessageDialog(this,"Error: "+e.getMessage(),"Encuesta",JOptionPane.ERROR_MESSAGE);
        }
        
        this.guardarDB(sSO,sPrg,sGrf,sAdm,iHrs);
        
    }//GEN-LAST:event_btnGuardarActionPerformed

