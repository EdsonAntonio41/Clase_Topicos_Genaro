    class Timing extends Thread {
        LocalTime fechahora;
        boolean display = true;
        @Override
        public void run() {
            try {
                while (true) {
                    this.fechahora = LocalTime.now();
                    Thread.sleep(1000);
                    if(display)
                        tfFechaHora.setText(this.fechahora.toString());
                }
            } catch (InterruptedException ex) {
                System.getLogger(Reloj.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }

        }
        
        public void displayOn() {
            this.display = true;
        }
        
        public void displayOff() {
            this.display = false;
        }   

    }
